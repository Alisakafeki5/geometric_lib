
def area(a):
    '''
    Возвращает площадь квадрата
    а: длина квадрата
    b: ширина квадрата

    Возвращаемое значение:
    a * a: искомая площадь квадрата
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата
    а: длина квадрата
    b: ширина квадрата

    Возвращаемое значение:
    4 * a: искомый периметр квадрата
    '''
    return 4 * a
